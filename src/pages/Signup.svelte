<script lang="ts">
  import { getContext } from "svelte";
  import { push } from "svelte-spa-router";
  import type { RegisterUser } from '../data/UserStore';

  const authService = getContext("AuthService");
  const newUser: RegisterUser = {email: '', firstName: '', lastName: '', password: '', isAdmin: false};

  async function signup() {
    let success = await authService.signup(newUser);
    if (success) {
      push("/login");
    } else {
      //errorMessage = "Error Trying to sign up";
    }
  }
</script>

<section class="columns">
  <div class="column is-half-desktop p-6 is-flex is-flex-direction-column is-align-items-stretch">
    <div class="is-flex is-justify-content-center">
      <img class="image is-180x180" src="/placemark.png" alt="Placemark logo">
    </div>
    <h1 class="title has-text-centered">Sign up</h1>
    <form on:submit|preventDefault={signup}>
      <label class="label">Name</label>
      <div class="field is-horizontal">
        <div class="field-body">
          <div class="field">
            <input bind:value={newUser.firstName} class="input" type="text" placeholder="Enter first name"
                   name="firstName">
          </div>
          <div class="field">
            <input bind:value={newUser.lastName} class="input" type="text" placeholder="Enter last name"
                   name="lastName">
          </div>
        </div>
      </div>
      <div class="field">
        <label class="label">Email</label>
        <input bind:value={newUser.email} class="input" type="text" placeholder="Enter email" name="email">
      </div>
      <div class="field">
        <label class="label">Password</label>
        <input bind:value={newUser.password} class="input" type="password" placeholder="Enter Password" name="password">
      </div>
      <label class="checkbox">
        <input bind:checked={newUser.isAdmin} type="checkbox" name="isAdmin">
        Register as admin
      </label>
      <div class="field pt-4 is-flex is-align-items-center">
        <button class="button is-link mr-4">Submit</button>
        <span>Already have an account? <a href="/#/login">Log in</a></span>
      </div>
    </form>
  </div>
  <div class="column is-half-desktop is-hidden-mobile p-0">
    <img class="background-img" src="/gps-location.svg" alt="map">
  </div>
